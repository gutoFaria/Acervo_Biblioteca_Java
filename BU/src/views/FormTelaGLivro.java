    private void btnAtualizarTccActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarTccActionPerformed
        int opcao = tabelaTcc.getSelectedRow();
        
        if(opcao >= 0){
            txtIdRegistroTcc.setText(tabelaTcc.getValueAt(opcao,0).toString());
            txtTituloTcc.setText(tabelaTcc.getValueAt(opcao,1).toString());
            txtAutorTcc.setText(tabelaTcc.getValueAt(opcao,2).toString());
            txtCursoTcc.setText(tabelaTcc.getValueAt(opcao,3).toString());
            txtTemaTcc.setText(tabelaTcc.getValueAt(opcao,4).toString());
            txtNumPaginasTcc.setText(tabelaTcc.getValueAt(opcao,5).toString());
            txtAnoTcc.setText(tabelaTcc.getValueAt(opcao,6).toString());
            
            
            btnAtualizaTcc.setVisible(true);
            btnSalvarTcc.setVisible(false);
        }else{
            JOptionPane.showMessageDialog(null,"Selecione uma linha!");
        }
    }//GEN-LAST:event_btnAtualizarTccActionPerformed

    private void btnTccExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTccExcluirActionPerformed
       int opcao = tabelaTcc.getSelectedRow();
       
       if(opcao >=0){
           //pega o id do registro
           tc.ExcluirTcc(Integer.parseInt(tabelaTcc.getValueAt(opcao,0).toString()));
           PreencherTabelaTcc();
       }else{
           JOptionPane.showMessageDialog(null,"Selecione uma linha!");
       }
    }//GEN-LAST:event_btnTccExcluirActionPerformed
