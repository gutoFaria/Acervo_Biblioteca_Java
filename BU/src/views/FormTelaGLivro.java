    private boolean ConfereLivro(){
        if(txtTituloLivro.getText().isEmpty() || txtAutorLivro.getText().isEmpty() || txtAssuntoLivro.getText().isEmpty() ||
                txtCidadeLivro.getText().isEmpty() || txtAnoLivro.getText().isEmpty() || txtCduLivro.getText().isEmpty()  ||
                txtNumPaginasLivro.getText().isEmpty() || txtEditoraLivro.getText().isEmpty() || txtNumEdicaoLivro.getText().isEmpty() ||
                txtIsbnLivro.getText().isEmpty()){
            return false;
        }
        return true;
    }
    
    private void CancelarLivro(){
        txtIdRegistroLivro.setText("");
        txtTituloLivro.setText("");
        txtAutorLivro.setText("");
        txtAssuntoLivro.setText("");
        txtCidadeLivro.setText("");
        txtAnoLivro.setText("");
        txtCduLivro.setText("");
        txtNumPaginasLivro.setText("");
        txtEditoraLivro.setText("");
        txtNumEdicaoLivro.setText("");
        txtIsbnLivro.setText("");
        
        btnAtualizaLivro.setVisible(false);
        btnSalvarLivro.setVisible(true);
    }
    
    private void PreencherTabelaLivro(){
        List<Livro> lista = lc.ListarLivros();
        
        DefaultTableModel modeloTabela = (DefaultTableModel) tabelaLivro.getModel();
        modeloTabela.setRowCount(0);
        
        for(Livro l : lista){
            modeloTabela.addRow(new Object[]{
                l.getIdRegistro(),
                l.getTitulo(),
                l.getAutores(),
                l.getAssunto(),
                l.getCidade(),
                l.getAno(),
                l.getCdu(),
                l.getNumPaginas(),
                l.getEditora(),
                l.getNumEdicao(),
                l.getIsbn()
            });
        }
    }
    private void btnSalvarLivroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarLivroActionPerformed
        boolean confere = ConfereLivro();
        
        if(confere){
            Livro l = new Livro();
            l.setTitulo(txtTituloLivro.getText());
            l.setAutores(txtAutorLivro.getText());
            l.setAssunto(txtAssuntoLivro.getText());
            l.setCidade(txtCidadeLivro.getText());
            l.setAno(txtAnoLivro.getText());
            l.setCdu(txtCduLivro.getText());
            l.setNumPaginas(Integer.parseInt(txtNumPaginasLivro.getText()));
            l.setEditora(txtEditoraLivro.getText());
            l.setNumEdicao(Integer.parseInt(txtNumEdicaoLivro.getText()));
            l.setIsbn(txtIsbnLivro.getText());
            
            lc.CadastrarLivro(l);
            CancelarLivro();
            PreencherTabelaLivro();
        }
    }//GEN-LAST:event_btnSalvarLivroActionPerformed

